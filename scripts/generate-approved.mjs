/**
 * scripts/generate-approved.mjs
 *
 * Reads data/store.json and generates src/lib/approvedWines.ts
 * so that approved producer wines are baked into the static build.
 *
 * Run automatically by `npm run build` via prebuild hook.
 */

import fs from "node:fs";
import path from "node:path";

const rootDir = process.cwd();
const storeFile = path.join(rootDir, "data", "store.json");
const outFile = path.join(rootDir, "src", "lib", "approvedWines.ts");

let approvedWines = [];

if (fs.existsSync(storeFile)) {
  try {
    const raw = JSON.parse(fs.readFileSync(storeFile, "utf-8"));
    approvedWines = Array.isArray(raw.approvedWines) ? raw.approvedWines : [];
  } catch {
    console.warn("[generate-approved] Could not parse data/store.json, skipping.");
  }
}

const ts = `// AUTO-GENERATED by scripts/generate-approved.mjs — do not edit
// Source: data/store.json (committed by backend via GitHub API)
import type { Wine } from "./types";

export const approvedWines: Wine[] = ${JSON.stringify(approvedWines, null, 2)};
`;

fs.writeFileSync(outFile, ts, "utf-8");
console.log(`[generate-approved] ${approvedWines.length} approved wine(s) written to src/lib/approvedWines.ts`);
